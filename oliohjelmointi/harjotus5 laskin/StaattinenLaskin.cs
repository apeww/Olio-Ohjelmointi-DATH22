using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace harjotus5_laskin
{
    static class StaattinenLaskin
    {
        public static int summa(int a, int b)
        {
            return a + b;
        }

        public static int vähennys(int a, int b)
        {
            return a - b;
        }

        public static int kerto(int a, int b)
        {
            return a * b;
        }

        public static int jako(int a, int b)
        {
            return a / b;
        }
    }
}
